-- shared_quote_form_links policies

CREATE POLICY "Agents can view own links"
  ON shared_quote_form_links FOR SELECT
  TO authenticated
  USING (agent_id = auth.uid());

CREATE POLICY "Gerentes can view office links"
  ON shared_quote_form_links FOR SELECT
  TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM usuarios u
      WHERE u.id = auth.uid()
        AND u.rol IN ('Gerente', 'Administrador')
        AND (u.rol = 'Administrador' OR u.oficina_id = office_id)
    )
  );

CREATE POLICY "Agents can insert own links"
  ON shared_quote_form_links FOR INSERT
  TO authenticated
  WITH CHECK (agent_id = auth.uid() OR created_by = auth.uid());

CREATE POLICY "Agents can update own links"
  ON shared_quote_form_links FOR UPDATE
  TO authenticated
  USING (agent_id = auth.uid())
  WITH CHECK (agent_id = auth.uid());

CREATE POLICY "Admins can update any link"
  ON shared_quote_form_links FOR UPDATE
  TO authenticated
  USING (
    EXISTS (SELECT 1 FROM usuarios WHERE id = auth.uid() AND rol = 'Administrador')
  )
  WITH CHECK (
    EXISTS (SELECT 1 FROM usuarios WHERE id = auth.uid() AND rol = 'Administrador')
  );

-- shared_quote_form_submissions policies

CREATE POLICY "Agents can view own submissions"
  ON shared_quote_form_submissions FOR SELECT
  TO authenticated
  USING (agent_id = auth.uid());

CREATE POLICY "Gerentes can view office submissions"
  ON shared_quote_form_submissions FOR SELECT
  TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM usuarios u
      WHERE u.id = auth.uid()
        AND u.rol IN ('Gerente', 'Administrador')
        AND (u.rol = 'Administrador' OR u.oficina_id = office_id)
    )
  );

-- Service role inserts submissions (edge function runs as service role)
CREATE POLICY "Service role can insert submissions"
  ON shared_quote_form_submissions FOR INSERT
  TO service_role
  WITH CHECK (true);

-- Allow service role to update submissions
CREATE POLICY "Service role can update submissions"
  ON shared_quote_form_submissions FOR UPDATE
  TO service_role
  USING (true)
  WITH CHECK (true);

-- Service role can also update shared links (increment counter)
CREATE POLICY "Service role can update links"
  ON shared_quote_form_links FOR UPDATE
  TO service_role
  USING (true)
  WITH CHECK (true);

-- Public read of active links by slug (for public form page)
CREATE POLICY "Public can read active links by slug"
  ON shared_quote_form_links FOR SELECT
  TO anon
  USING (status = 'active');

-- ─────────────────────────────────────────
-- Helper function: resolve agent branding snapshot
-- ─────────────────────────────────────────
CREATE OR REPLACE FUNCTION get_agent_brand_config(p_agent_id uuid)
RETURNS jsonb
LANGUAGE plpgsql SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_user RECORD;
  v_office RECORD;
  v_brand jsonb;
BEGIN
  SELECT nombre, apellido_paterno, apellido_materno, oficina_id, nombre_publico,
         correo_laboral, foto_perfil
    INTO v_user
    FROM usuarios
   WHERE id = p_agent_id;

  IF v_user.oficina_id IS NOT NULL THEN
    SELECT nombre, logo_url, color_acento INTO v_office
      FROM oficinas WHERE id = v_user.oficina_id;
  END IF;

  v_brand := jsonb_build_object(
    'agent_name',       COALESCE(v_user.nombre_publico, v_user.nombre || ' ' || COALESCE(v_user.apellido_paterno, '')),
    'office_name',      COALESCE(v_office.nombre, ''),
    'primary_color',    COALESCE(v_office.color_acento, '#2563eb'),
    'secondary_color',  '#1e40af',
    'logo_url',         COALESCE(v_user.foto_perfil, v_office.logo_url, '/logojiro.png'),
    'footer_text',      'Formulario enviado a través de MOVI Digital.'
  );

  RETURN v_brand;
END;
$$;

GRANT EXECUTE ON FUNCTION get_agent_brand_config(uuid) TO authenticated, anon, service_role;
